import { describe, expect, it } from "vitest";

import { formatGeneratedFile, validateMessages } from "./i18n-utils.js";

const validMessages = {
  en: {
    nav: {
      stores: "Stores",
    },
  },
  hr: {
    nav: {
      stores: "Trgovine",
    },
  },
};

describe("i18n generator utilities", () => {
  it("validates matching locale message trees", () => {
    expect(validateMessages(validMessages)).toEqual(validMessages);
  });

  it("rejects invalid locale identifiers", () => {
    expect(() =>
      validateMessages({
        en: { title: "Title" },
        "HR-hr": { title: "Naslov" },
      }),
    ).toThrow('Invalid locale "HR-hr"');
  });

  it("rejects messages without the default locale", () => {
    expect(() => validateMessages({ hr: { title: "Naslov" } })).toThrow(
      'messages.json must contain the default "en" locale.',
    );
  });

  it("rejects missing translated keys", () => {
    expect(() =>
      validateMessages({
        en: { nav: { stores: "Stores" }, stores: { title: "Stores" } },
        hr: { nav: { stores: "Trgovine" } },
      }),
    ).toThrow("hr missing key: stores.title");
  });

  it("rejects extra translated keys", () => {
    expect(() =>
      validateMessages({
        en: { nav: { stores: "Stores" } },
        hr: { nav: { stores: "Trgovine" }, stores: { title: "Trgovine" } },
      }),
    ).toThrow("hr has extra key: stores.title");
  });

  it("formats the generated i18n module", () => {
    expect(formatGeneratedFile(validMessages)).toBe(`// Generated by build/generate-i18n.ts. Do not edit directly.

export const defaultLocale = "en";

export const messages = {
  en: {
    nav: {
      stores: "Stores",
    },
  },
  hr: {
    nav: {
      stores: "Trgovine",
    },
  },
} as const;
`);
  });
});
