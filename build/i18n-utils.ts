export const defaultLocale = "en";

const localePattern = /^[a-z]{2,3}(?:-[a-z0-9]{2,8})*$/;

export type MessageValue = string | MessageTree;
export type MessageTree = { [key: string]: MessageValue };
export type Messages = Record<string, MessageTree>;

function isRecord(value: unknown): value is Record<string, unknown> {
  return Boolean(value) && typeof value === "object" && !Array.isArray(value);
}

function validateMessageTree(value: unknown, path: string): value is MessageTree {
  if (!isRecord(value)) {
    throw new Error(`${path} must be an object.`);
  }

  for (const [key, childValue] of Object.entries(value)) {
    const childPath = `${path}.${key}`;

    if (typeof childValue === "string") {
      continue;
    }

    validateMessageTree(childValue, childPath);
  }

  return true;
}

function collectKeys(tree: MessageTree, prefix = "") {
  const keys: string[] = [];

  for (const [key, value] of Object.entries(tree)) {
    const path = prefix ? `${prefix}.${key}` : key;

    if (typeof value === "string") {
      keys.push(path);
      continue;
    }

    keys.push(...collectKeys(value, path));
  }

  return keys;
}

function validateLocaleKeys(messages: Messages) {
  const defaultKeys = new Set(collectKeys(messages[defaultLocale]));

  for (const [locale, tree] of Object.entries(messages)) {
    if (locale === defaultLocale) {
      continue;
    }

    const localeKeys = new Set(collectKeys(tree));
    const missingKeys = [...defaultKeys].filter((key) => !localeKeys.has(key));
    const extraKeys = [...localeKeys].filter((key) => !defaultKeys.has(key));

    if (missingKeys.length || extraKeys.length) {
      const errors = [
        ...missingKeys.map((key) => `${locale} missing key: ${key}`),
        ...extraKeys.map((key) => `${locale} has extra key: ${key}`),
      ];

      throw new Error(errors.join("\n"));
    }
  }
}

export function validateMessages(value: unknown): Messages {
  if (!isRecord(value)) {
    throw new Error("messages.json root must be an object.");
  }

  if (!(defaultLocale in value)) {
    throw new Error(`messages.json must contain the default "${defaultLocale}" locale.`);
  }

  const messages: Messages = {};

  for (const [locale, tree] of Object.entries(value)) {
    if (!localePattern.test(locale)) {
      throw new Error(`Invalid locale "${locale}". Use lowercase locale identifiers like "en" or "en-gb".`);
    }

    if (validateMessageTree(tree, locale)) {
      messages[locale] = tree;
    }
  }

  validateLocaleKeys(messages);

  return messages;
}

function formatPropertyKey(key: string) {
  return /^[A-Za-z_$][\w$]*$/.test(key) ? key : JSON.stringify(key);
}

function formatObject(value: MessageTree | Messages, indent = 0): string {
  const indentation = " ".repeat(indent);
  const childIndentation = " ".repeat(indent + 2);
  const entries = Object.entries(value).map(([key, childValue]) => {
    const formattedValue =
      typeof childValue === "string" ? JSON.stringify(childValue) : formatObject(childValue, indent + 2);

    return `${childIndentation}${formatPropertyKey(key)}: ${formattedValue},`;
  });

  return `{\n${entries.join("\n")}\n${indentation}}`;
}

export function formatGeneratedFile(messages: Messages) {
  return `// Generated by build/generate-i18n.ts. Do not edit directly.

export const defaultLocale = ${JSON.stringify(defaultLocale)};

export const messages = ${formatObject(messages)} as const;
`;
}
